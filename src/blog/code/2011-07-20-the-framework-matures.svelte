<p>
	Being a developer requires one very important thing: staying current with technology. I don't even
	know how many versions of my framework I have created over the course of the last seven months.
	Some people believe that I should follow standards and go with a more common framework such as
	CodeIgniter or CakePHP. Although those frameworks are excellent at what they do, unfortunately
	they do not benefit me in my current situation.
</p>

<p>
	I wrote my own framework for only about five reasons: One, at the time the first framework I ever
	used was way too big and I learned that I only utilized maybe half or less than half of the
	folders there. Two, I wanted a framework that worked in sub directories (at the time I did not
	know about CodeIgniter - which really is a great framework I will recommend anyone but myself).
	Three, I needed structure because at the time I was used to referencing libraries, models,
	controllers, and whatever else as "$this-&gt;class" what is this? Is this a controller, a view, a
	model? I didn't know and it was majorly confusing. Four, I thought having to say
	"/app/appname/app/img/test.jpg" was way too long. I don't like absolute paths because that meant
	each time I wanted a new site in my directory, I would have to say "/sitename/app/appname..." and
	that was not fun having to change all those paths once I deployed to server.
</p>

<p>
	Lastly, I wanted to learn. Writing an MVC framework over the last 7 months has really made me
	understand the importance in structure. The goal should always be simplicity. Everything I do I
	try to make easy for me to understand now and later. I think I finally have finished what I
	originally wanted to do with MVC and so far it's been great for all the projects I have been
	working on. Sure it's been a battle and i've had some scary moments (as everyone does when
	learning something), but it's a battle I am willing to learn from and continue. It's my engine and
	for what it's worth - it's not so bad, it's actually everything i've wanted it to be.
</p>

