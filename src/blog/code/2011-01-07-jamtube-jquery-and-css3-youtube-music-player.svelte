<p>
	JamTube is now ready and rockin'! The idea is simple: I wanted to create a way to generate a
	youtube playlist while I browsed or surfed the web. Since I was using Linux based computer with no
	ITunes, I would always go from one video to another on YouTube just listening to tracks. Now,
	although I could have created a music playlist on YouTube itself, I was too lazy... but then I
	came across the wild idea of why not share this playlist on my site?
</p>

<p>
	One thing lead to another and soon I was on my way to creating little animations and playlists
	that could be shared via facebook. It's a fun idea, and you can actually see it working right now
	on this blog. Just another fun coding project :)
</p>
