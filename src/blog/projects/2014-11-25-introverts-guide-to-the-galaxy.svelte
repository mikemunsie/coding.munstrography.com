<h1>I'm Writing a Book/Blog!</h1>

<p>
	It's about time! I've always enjoyed writing and I finally got the courage to write my first
	book/blog (blook as my dad calls it). It's a humorous little read about daily experiences in the
	life of an introvert.
</p>

<p>
	I've realized that I have a few quirks, and apparently I'm not the only one! These experiences are
	your everyday normal experiences, but magnified through the eyes of an introverted person. I had a
	lot of fun writing this book and I haven't finished yet. I plan on continuing to add content as
	the months progress and hopefully get a few people to help add in their stories.
</p>

