<h1>Material Design is fun</h1>

<p>
	I've been wanting to play around with material design for a while now, and I finally got the
	chance in the last few days thanks to a nice once-in-a-year cold. I really liked the idea of "lets
	animate all the things". I downloaded the angular-material framework
	(https://github.com/angular/material) and then had some fun.
</p>

<p>
	The idea of the application simple: manage your monthly budget. You input your salaries, add in
	your bills, and it will tell you how much you will save and how much of your salary goes towards
	bills. Simple as that.
</p>

<p>
	What's cool about this web mobile app is all the animations. I'd say about 80% of the time that
	went into this was creating custom animations, improving upon the material framework (such as icon
	colors), picking a good simple stack for future projects (decided to keep it NodeJS with a custom
	gulp build), and lastly some color config thanks to the good ol' dribbble.
</p>

